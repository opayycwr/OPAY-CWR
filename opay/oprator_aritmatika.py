X = 5
y = 3

#penjumblahan (+)
print("penjumblahan =", X + y)
#pengurangan (-)
print("pengngurangan =", X - y)
#perkalian (*)
print("perkalian =", X * y)
#pembagian (/)
print("pembagian =", X / y)
# modulus (%)
print("modulus =", X % y)
# perangkat (**)
print("perpangkatan =", X**y ) 
# pembagian bulat (//)
print("pembagian =", X//y)